#include <iostream>
using namespace std;

int main() 
{
	int i=0;
	cout<<"While: "<<endl;
	while(i<9)
	{
	    cout<<"Hello World"<<endl;
	    i++;
	}
	i=0;
	cout<<"Do while: "<<endl;
	do
	{
	    cout<<"Hello World"<<endl;
	    i++;
	}while(i<9);
	cout<<"For : "<<endl;
	for(i=0;i<9;i++)
	{
	    cout<<"Hello World"<<endl;
	}
	return 0;
}











